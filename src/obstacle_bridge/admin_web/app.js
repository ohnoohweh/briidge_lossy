function onboardingListId(kind) {
  return kind === 'own' ? 'onboardingOwnServicesList' : 'onboardingRemoteServicesList';
}

function onboardingDraftKey(kind) {
  return kind === 'own' ? 'ownServersDraft' : 'remoteServersDraft';
}

function readOnboardingSpecsFromDom(kind) {
  const root = document.getElementById(onboardingListId(kind));
  if (!root) return [];
  const itemNodes = Array.from(root.querySelectorAll('[data-service-item]'));
  return itemNodes.map((itemNode, index) => parseServiceSpecItem(itemNode, index));
}

function renderOnboardingServiceList(kind) {
  const root = document.getElementById(onboardingListId(kind));
  if (!root) return;
  const key = onboardingDraftKey(kind);
  const specs = sanitizeServiceSpecs(uiState.onboarding[key]);
  root.innerHTML = specs.map((spec, index) => renderServiceCatalogItemHtml(spec, index)).join('');
  root.querySelectorAll('[data-service-item]').forEach((itemNode) => {
    const rowIndex = Number(itemNode.getAttribute('data-service-item'));
    ['listen', 'target'].forEach((endpointKind) => {
      const protocolInput = itemNode.querySelector(`[data-service-field="${endpointKind}.protocol"]`);
      if (!protocolInput) return;
      setServiceEndpointMode(root, rowIndex, endpointKind, protocolInput.value);
      protocolInput.addEventListener('change', () => {
        setServiceEndpointMode(root, rowIndex, endpointKind, protocolInput.value);
      });
    });
    itemNode.querySelectorAll('.service-editor-input').forEach((input) => {
      input.addEventListener('input', () => setOnboardingMessage(''));
    });
    const removeBtn = itemNode.querySelector('[data-service-remove]');
    removeBtn?.addEventListener('click', () => {
      try {
        const current = readOnboardingSpecsFromDom(kind);
        current.splice(rowIndex, 1);
        uiState.onboarding[key] = current;
        renderOnboardingServiceList(kind);
      } catch (error) {
        setOnboardingMessage(`Service draft has validation errors: ${error}`);
      }
    });
  });
}

