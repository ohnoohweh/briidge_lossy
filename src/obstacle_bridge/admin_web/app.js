async function applyOnboardingInvite() {
  try {
    if (!uiState.onboarding.invitePreview) {
      await setupAssistantNextFromToken();
      if (!uiState.onboarding.invitePreview) return;
    }
    const updates = { ...(uiState.onboarding.suggestedUpdates || {}) };
    const exposure = String(document.getElementById('onboardingAdminExposure')?.value || 'local').toLowerCase();
    updates.admin_web_bind = resolveOnboardingAdminBind(exposure);
    const credState = getOnboardingAdminCredentialState();
    if (credState.hasAnyTypedCredPart && !credState.hasTypedCreds) {
      setOnboardingMessage('Set both admin username and password, or leave both empty.');
      return;
    }
    if (credState.hasTypedCreds) {
      updates.admin_web_auth_disable = false;
      updates.admin_web_username = credState.username;
      updates.admin_web_password = credState.password;
    } else if (!credState.reuseExisting) {
      updates.admin_web_auth_disable = true;
      updates.admin_web_username = '';
      updates.admin_web_password = '';
    }
    if (Object.keys(updates).length === 0) {
      setOnboardingMessage('Invite has no applicable settings to apply.');
      return;
    }
    const ok = await saveConfigUpdates(
      updates,
      `Applied ${Object.keys(updates).length} invite-derived setting(s).`,
      { restartAfterSave: true, skipLoadAfterSave: true }
    );
    if (!ok) {
      const detail = String(document.getElementById('configMessage')?.textContent || '').trim();
      setOnboardingMessage(`Finish did not persist configuration. ${detail || 'Please retry and confirm password if prompted.'}`);
      return;
    }

    setOnboardingMessage('Configuration persisted and restart requested.', true);
    closeSetupAssistantGate();
  } catch (error) {
    setOnboardingMessage(`Apply invite failed: ${error}`);
  }
}

async function copyOnboardingInvite() {
  const output = document.getElementById('onboardingInviteOutput');
  const token = String(output?.value || '').trim();
  if (!token) {
    setOnboardingMessage('Generate an invite token first.');
    return;
  }
  try {
    await navigator.clipboard.writeText(token);
    setOnboardingMessage('Invite token copied to clipboard.', true);
  } catch (_error) {
    setOnboardingMessage('Clipboard copy failed. Select and copy the token manually.');
  }
}

