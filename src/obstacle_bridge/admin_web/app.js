async function applyManualSetup() {
  try {
    const plan = buildManualSetupPlan();
    const ok = await saveConfigUpdates(
      plan.updates,
      `Applied ${Object.keys(plan.updates).length} manual configuration value(s).`,
      { restartAfterSave: true, skipLoadAfterSave: true },
    );
    if (!ok) {
      const detail = String(document.getElementById('configMessage')?.textContent || '').trim();
      setOnboardingMessage(`Manual setup did not persist configuration. ${detail || 'Please retry and confirm password if prompted.'}`);
      return;
    }
    setOnboardingMessage('Manual configuration persisted and restart requested.', true);
    closeManualSetupGate();
  } catch (error) {
    setOnboardingMessage(`Manual setup apply failed: ${error}`);
  }
}

