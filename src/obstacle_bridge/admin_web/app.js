function startPolling(task, intervalMs) {
  let stopped = false;

  async function tick() {
    while (!stopped) {
      if (!isApiEnabled()) {
        await sleep(intervalMs);
        continue;
      }
      const startedAt = Date.now();
      try {
        await task();
      } catch (e) {
        console.error('poll task failed', e);
      }
      const elapsedMs = Date.now() - startedAt;
      const waitMs = Math.max(0, intervalMs - elapsedMs);
      await sleep(waitMs);
    }
  }

  tick();
  return () => {
    stopped = true;
  };
}

