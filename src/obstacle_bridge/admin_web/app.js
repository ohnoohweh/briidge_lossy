function sanitizeManualSetupPreview(value) {
  const clone = JSON.parse(JSON.stringify(value || {}));
  if (Object.prototype.hasOwnProperty.call(clone, 'secure_link_psk')) {
    clone.secure_link_psk = clone.secure_link_psk ? '***hidden***' : '';
  }
  if (Object.prototype.hasOwnProperty.call(clone, 'admin_web_password')) {
    clone.admin_web_password = clone.admin_web_password ? '***hidden***' : '';
  }
  return clone;
}

function applySetupAssistantAdminAccess(updates) {
  const next = { ...(updates || {}) };
  const exposure = String(document.getElementById('onboardingAdminExposure')?.value || 'local').toLowerCase();
  const credState = getOnboardingAdminCredentialState();
  next.admin_web = true;
  next.admin_web_bind = resolveOnboardingAdminBind(exposure);
  let adminCredentialsSource = 'wizard_disables_auth';
  if (credState.hasAnyTypedCredPart && !credState.hasTypedCreds) {
    throw new Error('Set both admin username and password, or leave both empty.');
  }
  if (credState.hasTypedCreds) {
    adminCredentialsSource = 'wizard_input_only';
    next.admin_web_auth_disable = false;
    next.admin_web_username = credState.username;
    next.admin_web_password = credState.password;
  } else if (credState.reuseExisting) {
    adminCredentialsSource = 'existing_startup_credentials';
    next.admin_web_auth_disable = false;
    next.admin_web_username = credState.existingUsername;
  } else {
    next.admin_web_auth_disable = true;
    next.admin_web_username = '';
    next.admin_web_password = '';
  }
  return { updates: next, adminCredentialsSource };
}

function parseManualSetupPort(rawValue, fieldLabel, { allowZero = false } = {}) {
  const text = String(rawValue || '').trim();
  const normalized = text.toLowerCase();
  if (allowZero && (text === '' || normalized === '(dynamic)' || normalized === 'dynamic')) {
    return 0;
  }
  if (!text) {
    throw new Error(`${fieldLabel} is required.`);
  }
  const value = Number(text);
  if (!Number.isInteger(value) || value < 0 || value > 65535 || (!allowZero && value === 0)) {
    throw new Error(`${fieldLabel} must be a valid port number${allowZero ? ' from 0 to 65535' : ' from 1 to 65535'}.`);
  }
  return value;
}

function parseConfigSampleText(rawValue) {
  const raw = String(rawValue || '').trim();
  if (!raw) {
    throw new Error('Paste a README JSON sample first.');
  }
  let parsed;
  try {
    parsed = JSON.parse(raw);
  } catch (error) {
    throw new Error(`Manual JSON parse failed: ${error}`);
  }
  if (!parsed || typeof parsed !== 'object' || Array.isArray(parsed)) {
    throw new Error('Manual import expects a JSON object with runtime config keys at the top level.');
  }
  if (parsed.config && typeof parsed.config === 'object' && !Array.isArray(parsed.config)) {
    return parsed.config;
  }
  if (parsed.updates && typeof parsed.updates === 'object' && !Array.isArray(parsed.updates)) {
    return parsed.updates;
  }
  return parsed;
}

function buildManualSetupGuidedUpdates() {
  const role = getManualSetupRole();
  const protocol = getManualSetupProtocol();
  const prefix = protocol === 'myudp' ? 'udp' : protocol;
  const bind = String(document.getElementById('manualSetupBind')?.value || '').trim() || String(manualSetupDefaultValues(role, protocol).bind || '::');
  const ownPort = parseManualSetupPort(document.getElementById('manualSetupOwnPort')?.value, 'Own port', { allowZero: role === 'client' });
  const instanceName = String(document.getElementById('manualSetupInstanceName')?.value || '').trim();
  const updates = {
    overlay_transport: protocol,
    admin_web_name: instanceName || String(configState.config?.admin_web_name || `${role === 'server' ? 'Peer Server' : 'Peer Client'} ${protocol.toUpperCase()}`),
  };
  updates[`${prefix}_bind`] = bind;
  updates[`${prefix}_own_port`] = ownPort;
  if (role === 'client') {
    const peerHost = String(document.getElementById('manualSetupPeerHost')?.value || '').trim();
    if (!peerHost) {
      throw new Error('Peer FQDN or IP is required for a client setup.');
    }
    updates[`${prefix}_peer`] = peerHost;
    updates[`${prefix}_peer_port`] = parseManualSetupPort(document.getElementById('manualSetupPeerPort')?.value, 'Peer port');
  } else {
    updates[`${prefix}_peer`] = '';
    updates[`${prefix}_peer_port`] = 0;
  }
  if (protocol === 'ws') {
    const wsPayloadMode = String(document.getElementById('manualSetupWsPayloadMode')?.value || 'binary').trim().toLowerCase();
    if (!MANUAL_SETUP_WS_PAYLOAD_MODES.includes(wsPayloadMode)) {
      throw new Error('WS payload mode is invalid.');
    }
    updates.ws_payload_mode = wsPayloadMode;
  }
  return { role, protocol, updates };
}

