async function generateOnboardingInvite() {
  try {
    const ownServers = readOnboardingSpecsFromDom('own');
    const remoteServers = readOnboardingSpecsFromDom('remote');
    uiState.onboarding.ownServersDraft = ownServers;
    uiState.onboarding.remoteServersDraft = remoteServers;
    const select = document.getElementById('onboardingConnectionSelect');
    const connectionId = String(select?.value || '').trim();
    const r = await apiFetch('/api/onboarding/invite/generate', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        connection_id: connectionId,
        own_servers: ownServers,
        remote_servers: remoteServers,
      }),
    });
    const j = await r.json();
    if (!r.ok || !j.ok) {
      if (r.status === 409 && Array.isArray(j.profiles)) {
        uiState.onboarding.profiles = j.profiles;
        renderOnboardingConnectionProfiles();
      }
      throw new Error(j.error || `HTTP ${r.status}`);
    }
    const output = document.getElementById('onboardingInviteOutput');
    if (output instanceof HTMLTextAreaElement) {
      output.value = String(j.invite_token || '');
    }
    renderTokenGeneratorReview();
    setOnboardingMessage('Invite token generated from current settings.', true);
  } catch (error) {
    setOnboardingMessage(`Invite generation failed: ${error}`);
  }
}

async function previewOnboardingInvite() {
  const input = document.getElementById('onboardingInviteInput');
  const token = String(input?.value || '').trim();
  if (!token) {
    setOnboardingMessage('Paste an invite token first.');
    return;
  }
  try {
    const r = await apiFetch('/api/onboarding/invite/preview', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ invite_token: token }),
    });
    const j = await r.json();
    if (!r.ok || !j.ok) throw new Error(j.error || `HTTP ${r.status}`);
    uiState.onboarding.invitePreview = j.preview || null;
    uiState.onboarding.suggestedUpdates = j.suggested_updates || {};
    const safeSuggested = { ...(j.suggested_updates || {}) };
    if (Object.prototype.hasOwnProperty.call(safeSuggested, 'secure_link_psk')) {
      safeSuggested.secure_link_psk = '***hidden***';
    }
    const previewNode = document.getElementById('onboardingInvitePreview');
    if (previewNode) {
      previewNode.textContent = JSON.stringify({
        connection: j.preview?.connection || {},
        secure_link_mode: j.preview?.secure_link_mode || 'off',
        secure_link_psk_present: Boolean(j.preview?.secure_link_psk_present),
        suggested_updates: safeSuggested,
      }, null, 2);
    }
    setOnboardingMessage('Invite token parsed. Review and apply when ready.', true);
  } catch (error) {
    setOnboardingMessage(`Invite preview failed: ${error}`);
  }
}

