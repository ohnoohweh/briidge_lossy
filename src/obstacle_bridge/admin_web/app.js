async function loadOnboardingResources() {
  const [profilesResp, blueprintsResp] = await Promise.all([
    apiFetch('/api/onboarding/connection-profiles', { cache: 'no-store' }),
    apiFetch('/api/onboarding/blueprints', { cache: 'no-store' }),
  ]);
  const profilesDoc = await profilesResp.json();
  const blueprintsDoc = await blueprintsResp.json();
  if (!profilesResp.ok || !profilesDoc.ok) {
    throw new Error(profilesDoc.error || `HTTP ${profilesResp.status}`);
  }
  if (!blueprintsResp.ok || !blueprintsDoc.ok) {
    throw new Error(blueprintsDoc.error || `HTTP ${blueprintsResp.status}`);
  }
  uiState.onboarding.profiles = Array.isArray(profilesDoc.profiles) ? profilesDoc.profiles : [];
  uiState.onboarding.blueprints = Array.isArray(blueprintsDoc.blueprints) ? blueprintsDoc.blueprints : [];
  renderOnboardingConnectionProfiles();
  renderOnboardingBlueprints();
}

